public static boolean isPrime(int n) {
        if (n <= 1) { // Если n меньше или равно 1, то это не является простым числом
        return false;
        }
        for (int i = 2; i <= Math.sqrt(n); i++) { // Проверяем делители до квадратного корня из n
        if (n % i == 0) { // Если i является делителем n, то n не является простым числом
        return false;
        }
        }
        return true; // Если делители не были найдены, то n является простым числом
        }
